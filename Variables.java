public class Variables{
    public static void main(String[] args) {

        // Declaring variables
        int number = 10;
        String name = "John";
        double height = 5.9;

        // Printing the variables
        System.out.println("Number: " + number);
        System.out.println("Name: " + name);
        System.out.println("Height: " + height);
    }
}
